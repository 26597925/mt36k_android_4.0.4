package com.mediatek.dlna.object;

/**
 * This Enum use to indicate the current object content type
 */
public enum ContentType {
    /**
     *  The object is photo
     */
    Photo,
     /**
     * The object is audio
     */
    Audio,
     /**
     * The object is video
     */
    Video,
     /**
     * The object is playlist
     */
    Playlist,
     /**
     * The object is item, such as XML,TXT
     */
    Item,
     /**
     * The object is directory
     */
    Directory
}
